package org.javaacademy.core.module2.homework3.ex2;

public class Human {

    private int height;
    private int age;

    public Human(int height, int age) {
        this.height = height;
        this.age = age;
    }

    public int getHeight() {
        return height;
    }

    public int getAge() {
        return age;
    }
}
